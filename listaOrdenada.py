#Lista ordenada
minha_lista1 = [125,398,450,365,378,393,984,1,2,3,6,12,15]
minha_lista2 = ["Abacate", "Zinco", "Pipoca", "Guará", "Açaí", "Quentão", "Dinheiro"]

#Ordenar os numeros
minha_lista1.sort()
print(minha_lista1)

#sort serve para ordenar alfabeticamente
minha_lista2.sort()
print(minha_lista2)

#Ordenar decrescente
minha_lista1.sort(reverse=True)
print(minha_lista1)

#Invertar a lista
minha_lista1.reverse()
print(minha_lista1)
